#As strings em python são cercadas por aspas únicas ou aspas duplas.
print("Hello, World"); #Hello, World
print('Hello, World'); #Hello, World

#Atribuindo uma string a variável
text =  "texto qualquer...";
print(text); #texto qualquer...

#Você pode atribuir uma sequência multiline a uma variável usando três cotações:
text2 = """Lorem ipsum dolor sit amet,
consectetur adipiscing elit,
sed do eiusmod tempor incididunt. """
print(text2);

#Ou três citações simples:
text3 = '''Lorem ipsum dolor sit amet,
consectetur adipiscing elit,
sed do eiusmod tempor incididunt.'''
print(text3);

#Strings são arrays de caracteres 
nome = "John";
print(nome[0]); #j
print(nome[1]); #o
