#Para combinar texto e variável, Python usa o + caractere:
x = "legal";

print("Python é " + x); #saída: Python é legal

#Você também pode usar o +caractere para adicionar uma variável a outra variável:
y = "Python é ";
z = "legal";

frase  = y + z;

print(frase); #saída: Python é legal

#Para números, o +personagem funciona como um operador matemático:

valor1 = 10;
valor2 = 10;

print(valor1 + valor2); #saída: 20

