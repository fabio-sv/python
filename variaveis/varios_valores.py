#Python permite que você atribua valores a várias variáveis ​​em uma linha
x, y, z = "João", "Maria", "Pedro";

print(x); #saída: João
print(y); #saída: Maria
print(z); #saída: Pedro

#E você pode atribuir o mesmo valor a várias variáveis ​​em uma linha:
x1 = y1 = z1 = "Laranja";

print(x1); #saída: Laranja
print(y1); #saída: Laranja
print(z1); #saída: Laranja
